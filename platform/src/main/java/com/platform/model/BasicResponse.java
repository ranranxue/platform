package com.platform.model;

public class BasicResponse {
	private int code;
	private String msg;
	
	public BasicResponse(){}
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}

}
